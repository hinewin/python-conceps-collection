### a list contains set of data

table1= ["apple",1,2.5,"cow"]

#### To access a list use its range

print (table1[0]) ### this will print out apple

### to check the length of the list use LEN

print (len(table1)) ## Will print out 4

## You can also assign a variable to the position of the list

animal = table1[-1] ### Grab the very last one of the list

print (animal)

